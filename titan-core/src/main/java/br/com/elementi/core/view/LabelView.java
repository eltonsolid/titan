package br.com.elementi.core.view;

public class LabelView {

	private String label;
	private String value;

	public LabelView(String label, String value) {
		super();
		this.label = label;
		this.value = value;
	}

	public String getLabel() {
		return label;
	}

	public String getValue() {
		return value;
	}

}
